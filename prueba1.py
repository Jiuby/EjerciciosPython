x, y = input().split(" ")

print(int(x) + int(y))

# x y y son los valores que se van a sumar por lo tanto son las variables ( normalmente van separados pero para este caso que el input va junto se separan con split

# input es con la sintaxis de python para que el usuario pueda ingresar datos

# split es para separar los datos que se ingresan

# print es para mostrar el resultado de la suma de los valores que se ingresaron

# int es para que los valores ingresados sean enteros

# x + y es la suma de los valores que se ingresaron